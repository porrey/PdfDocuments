using PdfSharp.Drawing;
using PdfSharp.Pdf;

namespace PdfDocuments
{
	/// <summary>
	/// Provides an abstract base class for generating PDF documents from a specified model type. Defines the contract and
	/// extensibility points for customizing PDF creation, layout, and rendering behaviors.
	/// </summary>
	/// <remarks>Derive from this class to implement custom PDF generation logic for specific model types. The class
	/// exposes virtual methods for initializing styles, creating pages, adding content, and rendering sections, allowing
	/// for flexible customization of the PDF output. The associated style manager and debug mode can be configured to
	/// control document appearance and diagnostic features.</remarks>
	/// <typeparam name="TModel">The type of the document model used to generate the PDF. Must implement the IPdfModel interface.</typeparam>
	public abstract class PdfGenerator<TModel> : IPdfGenerator<TModel>
		where TModel : IPdfModel
	{
		/// <summary>
		/// Initializes a new instance of the PdfGenerator class using the specified style manager.
		/// </summary>
		/// <param name="styleManager">The style manager used to apply formatting and styles to PDF documents generated by this instance. Cannot be null.</param>
		public PdfGenerator(IPdfStyleManager<TModel> styleManager)
		{
			this.StyleManager = styleManager;
		}

		/// <summary>
		/// Gets or sets the style manager used to control PDF rendering styles for the model.
		/// </summary>
		/// <remarks>Use this property to customize appearance settings such as fonts, colors, and layout when
		/// generating PDF documents. Assigning a different style manager allows for flexible styling of PDF output based on
		/// application requirements.</remarks>
		public virtual IPdfStyleManager<TModel> StyleManager { get; set; }

		/// <summary>
		/// Gets the type of the document model associated with the current instance.
		/// </summary>
		public virtual Type DocumentModelType => typeof(TModel);

		/// <summary>
		/// Gets or sets the title of the document.
		/// </summary>
		public virtual string DocumentTitle { get; set; }

		/// <summary>
		/// Gets or sets the debug mode for the application.
		/// </summary>
		public virtual DebugMode DebugMode { get; set; } = DebugMode.None;

		/// <summary>
		/// Calculates the usable width of the specified PDF page after accounting for left and right trim margins.
		/// </summary>
		/// <remarks>This method subtracts the left and right trim margins from the total page width to provide the
		/// area available for content placement.</remarks>
		/// <param name="page">The PDF page for which to determine the usable width. Cannot be null.</param>
		/// <returns>The width of the page, in units, excluding the left and right trim margins.</returns>
		protected virtual XUnit PageWidth(PdfPage page) => page.Width - page.TrimMargins.Left - page.TrimMargins.Right;

		/// <summary>
		/// Calculates the usable height of a PDF page after accounting for the top and bottom trim margins.
		/// </summary>
		/// <remarks>This method is virtual and can be overridden to customize how the usable page height is
		/// determined. The returned value represents the area available for content placement, not including trim
		/// margins.</remarks>
		/// <param name="page">The PDF page for which to calculate the usable height. Cannot be null.</param>
		/// <returns>The height of the page, in units, excluding the top and bottom trim margins.</returns>
		protected virtual XUnit PageHeight(PdfPage page) => page.Height - page.TrimMargins.Top - page.TrimMargins.Bottom;

		/// <summary>
		/// Asynchronously builds a PDF document from the specified model and returns the result along with the generated PDF
		/// data.
		/// </summary>
		/// <remarks>The method creates a PDF document based on the provided model. The returned PDF data can be used
		/// for storage, transmission, or display. The operation is performed asynchronously and may involve I/O or processing
		/// delays.</remarks>
		/// <param name="model">The model containing the data to be used for generating the PDF document. Cannot be null.</param>
		/// <returns>A tuple containing a boolean indicating whether the PDF was successfully created, and a byte array with the PDF
		/// data. If creation fails, the byte array will be null.</returns>
		public virtual async Task<(bool, byte[])> BuildAsync(TModel model)
		{
			(bool result, byte[] pdf) = (false, null);

			//
			// Create the document.
			//
			using (PdfDocument document = new())
			{
				//
				// Get the document title.
				//
				this.DocumentTitle = await this.OnGetDocumentTitleAsync(model);

				//
				// Create the pages.
				//
				result = await this.OnCreatePdfAsync(document, model);

				//
				// Get the byte stream.
				//
				if (result)
				{
					pdf = await this.GetPdfByteArrayAsync(document, model);
				}
			}

			return (result, pdf);
		}

		/// <summary>
		/// Creates and renders the PDF document asynchronously using the specified model.
		/// </summary>
		/// <remarks>Override this method to customize the PDF creation process, including page layout, content
		/// generation, and rendering. This method is called as part of the PDF generation workflow and may be invoked
		/// multiple times for different documents.</remarks>
		/// <param name="document">The PDF document to be created and rendered.</param>
		/// <param name="model">The model containing data used to generate the content of the PDF document.</param>
		/// <returns>A task that represents the asynchronous operation. The task result is <see langword="true"/> if the PDF was
		/// created successfully; otherwise, <see langword="false"/>.</returns>
		protected virtual async Task<bool> OnCreatePdfAsync(PdfDocument document, TModel model)
		{
			bool returnValue = false;

			//
			// Initialize styles.
			//
			await this.OnInitializeStylesAsync(this.StyleManager);

			//
			// Create the PDF pages.
			//
			await this.OnCreatePagesAsync(document, model);

			//
			// Create the document sections.
			//
			IPdfSection<TModel> rootSection = await this.OnAddContentAsync();

			//
			// Render the pages.
			//
			int pageNumber = 1;

			foreach (PdfPage page in document.Pages)
			{
				//
				// Set page layout.
				//
				await this.OnSetPageLayoutAsync(page);

				//
				// Set grid layout.
				//
				PdfGrid grid = await this.OnSetPageGridAsync(page);

				//
				// Render the document page.
				//
				returnValue = await this.OnRenderDocumentAsync(rootSection, grid, document, page, pageNumber, model);

				//
				// Increment the page number.
				//
				pageNumber++;
			}

			return returnValue;
		}

		/// <summary>
		/// Initializes styles for PDF generation using the specified style manager.
		/// </summary>
		/// <param name="styleManager">The style manager used to configure and apply styles to the PDF model. Cannot be null.</param>
		/// <returns>A task that represents the asynchronous initialization operation.</returns>
		protected virtual Task OnInitializeStylesAsync(IPdfStyleManager<TModel> styleManager)
		{
			return Task.CompletedTask;
		}

		/// <summary>
		/// Retrieves the document title for the specified model asynchronously.
		/// </summary>
		/// <remarks>Override this method to provide a custom document title based on the model. The default
		/// implementation returns a placeholder title.</remarks>
		/// <param name="model">The model instance for which the document title is to be obtained.</param>
		/// <returns>A task that represents the asynchronous operation. The task result contains the document title as a string.</returns>
		protected virtual Task<string> OnGetDocumentTitleAsync(TModel model)
		{
			return Task.FromResult("No Document Title");
		}

		/// <summary>
		/// Creates and returns a grid layout for the specified PDF page asynchronously.
		/// </summary>
		/// <param name="page">The PDF page for which the grid layout is to be generated. Cannot be null.</param>
		/// <returns>A task that represents the asynchronous operation. The task result contains a grid layout for the specified PDF
		/// page.</returns>
		protected virtual Task<PdfGrid> OnSetPageGridAsync(PdfPage page)
		{
			return Task.FromResult(new PdfGrid(this.PageWidth(page).Point, this.PageHeight(page).Point, 200, 80));
		}

		/// <summary>
		/// Calculates the total number of pages required to display the specified model.
		/// </summary>
		/// <param name="model">The model instance for which the page count is to be determined. Cannot be null.</param>
		/// <returns>A task that represents the asynchronous operation. The task result contains the number of pages required for the
		/// given model.</returns>
		protected virtual Task<int> OnGetPageCountAsync(TModel model)
		{
			return Task.FromResult(1);
		}

		/// <summary>
		/// Creates pages in the specified PDF document based on the provided model.
		/// </summary>
		/// <remarks>Override this method to customize how pages are created for the document. The number of pages is
		/// determined by the model and may vary depending on its contents.</remarks>
		/// <param name="document">The PDF document to which pages will be added.</param>
		/// <param name="model">The model containing data used to determine the number of pages to create.</param>
		/// <returns>A task that represents the asynchronous operation.</returns>
		protected virtual async Task OnCreatePagesAsync(PdfDocument document, TModel model)
		{
			int pageCount = await this.OnGetPageCountAsync(model);

			for (int pageNumber = 1; pageNumber <= pageCount; pageNumber++)
			{
				document.AddPage();
			}
		}

		/// <summary>
		/// Configures the layout settings for the specified PDF page, including orientation, size, and trim margins.
		/// </summary>
		/// <remarks>Override this method to customize page layout settings for individual pages when generating a PDF
		/// document.</remarks>
		/// <param name="page">The PDF page to configure. Must not be null.</param>
		/// <returns>A task that represents the asynchronous operation. The task completes when the page layout has been set.</returns>
		protected virtual Task OnSetPageLayoutAsync(PdfPage page)
		{
			page.Orientation = PdfSharp.PageOrientation.Portrait;
			page.Size = PdfSharp.PageSize.Letter;
			page.TrimMargins.Left = XUnit.FromInch(.35);
			page.TrimMargins.Right = XUnit.FromInch(.35);
			page.TrimMargins.Top = XUnit.FromInch(.32);
			page.TrimMargins.Bottom = XUnit.FromInch(.32);

			return Task.FromResult(0);
		}

		/// <summary>
		/// Creates and returns a new PDF content section for the current model asynchronously.
		/// </summary>
		/// <remarks>Override this method to customize the content section added to the PDF. The default
		/// implementation returns a new, empty content section.</remarks>
		/// <returns>A task that represents the asynchronous operation. The task result contains a PDF section instance for the current
		/// model.</returns>
		protected virtual Task<IPdfSection<TModel>> OnAddContentAsync()
		{
			return Task.FromResult<IPdfSection<TModel>>(new PdfContentSection<TModel>());
		}

		/// <summary>
		/// Renders the specified PDF section onto the provided page asynchronously, applying layout and debug options as
		/// configured.
		/// </summary>
		/// <remarks>Rendering behavior may be affected by debug mode flags, which can reveal layout or grid details
		/// for troubleshooting. Override this method to customize section rendering or layout logic for derived
		/// classes.</remarks>
		/// <param name="section">The PDF section to render. Represents the content and layout instructions for the page.</param>
		/// <param name="grid">The grid layout used to position elements on the page. Influences how content is arranged during rendering.</param>
		/// <param name="document">The PDF document instance to which the page belongs. Used for context and resource management during rendering.</param>
		/// <param name="page">The PDF page where the section will be rendered. Must be a valid, initialized page within the document.</param>
		/// <param name="pageNumber">The zero-based index of the page within the document. Used for layout and numbering purposes.</param>
		/// <param name="model">The data model used to populate the section's content. Provides context and values for rendering.</param>
		/// <returns>A task that represents the asynchronous operation. The task result is <see langword="true"/> if the section was
		/// rendered successfully; otherwise, <see langword="false"/>.</returns>
		protected virtual async Task<bool> OnRenderDocumentAsync(IPdfSection<TModel> section, PdfGrid grid, PdfDocument document, PdfPage page, int pageNumber, TModel model)
		{
			bool returnValue = false;

			using (XGraphics gfx = XGraphics.FromPdfPage(page))
			{
				using (XForm form = new(document, this.PageWidth(page), this.PageHeight(page)))
				{
					using (XGraphics graphics = XGraphics.FromForm(form))
					{
						//
						// Create the page grid instance.
						//
						PdfGridPage gridPage = new()
						{
							DocumentTitle = this.DocumentTitle,
							Document = document,
							Page = page,
							Graphics = graphics,
							Grid = grid,
							PageNumber = pageNumber,
							DebugMode = this.DebugMode
						};

						//
						// Layout and render the sections.
						//
						if (await this.OnLayoutSectionsAsync(section, gridPage, model))
						{
							if (!this.DebugMode.HasFlag(DebugMode.HideDetails))
							{
								returnValue = await this.OnRenderSectionsAsync(section, gridPage, model);
							}
							else
							{
								returnValue = true;
							}
						}

						#region Debug Elements
						//
						// Draw extra items to help debug layout issues.
						//
						if (this.DebugMode.HasFlag(DebugMode.RevealLayout))
						{
							//
							// Have the primary section render the debug elements.
							//
							await section.RenderDebugAsync(gridPage, model);
						}

						if (this.DebugMode.HasFlag(DebugMode.RevealGrid))
						{
							//
							// Draw the grid for debugging.
							//
							gridPage.DrawGrid(XColor.FromArgb(60, XColors.Red), .3);
						}
						#endregion

						//
						// Draw the page.
						//
						gfx.DrawImage(form, page.TrimMargins.Left.Point, page.TrimMargins.Top.Point);
					}
				}
			}

			return returnValue;
		}

		/// <summary>
		/// Performs asynchronous layout of the specified PDF section within the given grid page using the provided model.
		/// </summary>
		/// <remarks>Override this method to customize how sections are laid out within a grid page. The section's
		/// bounds are set to the full page before layout. Rendering occurs only if the section's render condition is
		/// satisfied.</remarks>
		/// <param name="section">The PDF section to be laid out. Determines whether it should be rendered and manages its layout state.</param>
		/// <param name="grid">The grid page representing the layout context for the section. Provides bounds and rendering information.</param>
		/// <param name="m">The model instance containing data used for layout and rendering decisions.</param>
		/// <returns>A task that represents the asynchronous operation. The task result is <see langword="true"/> if the section was
		/// rendered; otherwise, <see langword="false"/>.</returns>
		protected virtual async Task<bool> OnLayoutSectionsAsync(IPdfSection<TModel> section, PdfGridPage grid, TModel m)
		{
			bool returnValue = false;

			//
			// Set the report section bounds to the full page.
			//
			section.ActualBounds = grid.Grid.GetBounds();

			//
			// Call layout on the primary section if it is being rendered.
			//
			if (section.ShouldRender.Resolve(grid, m))
			{
				if (await section.LayoutAsync(grid, m))
				{
					returnValue = true;
				}
			}

			return returnValue;
		}

		/// <summary>
		/// Renders the specified PDF section asynchronously on the given grid page using the provided model.
		/// </summary>
		/// <remarks>Override this method to customize how sections are rendered or to implement additional rendering
		/// logic. The section's bounds are set to the full page before rendering.</remarks>
		/// <param name="section">The PDF section to render. Determines layout and rendering behavior for the section.</param>
		/// <param name="grid">The grid page on which the section will be rendered. Provides layout and bounds information.</param>
		/// <param name="m">The model used to resolve rendering conditions and provide data for the section.</param>
		/// <returns>A task that represents the asynchronous operation. The task result is <see langword="true"/> if the section was
		/// rendered; otherwise, <see langword="false"/>.</returns>
		protected virtual async Task<bool> OnRenderSectionsAsync(IPdfSection<TModel> section, PdfGridPage grid, TModel m)
		{
			bool returnValue = false;

			//
			// Set the report section bounds to the full page.
			//
			section.ActualBounds = grid.Grid.GetBounds();

			//
			// Call layout on the primary section if it is being rendered.
			//
			if (section.ShouldRender.Resolve(grid, m))
			{
				if (await section.RenderAsync(grid, m))
				{
					returnValue = true;
				}
			}

			return returnValue;
		}

		/// <summary>
		/// Converts the specified PDF document to a byte array asynchronously.
		/// </summary>
		/// <param name="document">The PDF document to be converted to a byte array. Cannot be null.</param>
		/// <param name="model">The model containing data used for PDF generation. Cannot be null.</param>
		/// <returns>A task that represents the asynchronous operation. The task result contains a byte array of the PDF document
		/// contents.</returns>
		protected virtual Task<byte[]> GetPdfByteArrayAsync(PdfDocument document, TModel model)
		{
			byte[] returnValue = null;

			using (MemoryStream stream = new())
			{
				document.Save(stream, false);
				returnValue = stream.ToArray();
			}

			return Task.FromResult(returnValue);
		}
	}
}
